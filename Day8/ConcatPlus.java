class ConcatPlus {
  public static void main(String[] args) {


    String g = "Hello! ";
    System.out.println("First String: " + g);

    String name = "World";
    System.out.println("Second String: " + name);

    
    String joinedString = g + name;
    System.out.println("Joined String: " + joinedString);
  }
}